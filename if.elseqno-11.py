# write a python program to input week number and print week day.
weekday=int(input("enter a number 1-7"))
if weekday==1:
    print("it is sunday")
elif weekday==2:
    print("it is monday")
elif weekday==3:
    print("it is tuesday")
elif weekday==4:
    print("it is wednesday")
elif weekday==5:
    print("it is thursday")
elif weekday==6:
    print("it is friday")
elif weekday==7:
    print("it is saturday")
else:
    print("no weekday")
    